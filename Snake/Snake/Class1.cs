using System;
using System.Collections.Generic;
using System.Text;

namespace Snake1
{
    class Stydent
    {
        private string name;
        private int age;
        private string curse;
        private int specialty;

public Stydent(string name_)
        {
            name = name_;
        }

        public Stydent(string name_, int age_)
        {
            name = name_;
            age = age_;
        }
        public Stydent(string name_, int age_, string curse_, int specialty_)
        {
            name = name_;
            age = age_;
            curse = curse_;
            specialty = specialty_;
        }

        

    }
}
